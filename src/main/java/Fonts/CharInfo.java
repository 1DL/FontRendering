package Fonts;

public class CharInfo {
    public int sourceX;
    public int sourceY;
    public int width;
    public int height;

    public CharInfo(int sourceX, int sourceY, int width, int height) {
        this.sourceX = sourceX;
        this.sourceY = sourceY;
        this.width = width;
        this.height = height;
    }
}
